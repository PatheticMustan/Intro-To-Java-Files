public class Lesson8 {
	public static void main(String[] args) {
		// X, Y, and Z.
		double x = 6.2;
		double y = 4.54;
		double z = x - y;
		System.out.println(x + " - " + y + " = " + z);

		// C is the product of A and B.
		int a = 666;
		int b = 365;
		int c = a * b;
		System.out.println(a + " * " + b + " = " + c);

		// C is around the quotient of A and B.
		c = a / b;
		System.out.println(a + " / " + b + " = " + c);

		// C is the remainder of A and B.
		c = a % b;
		System.out.println(a + " % " + b + " = " + c);
	}
}